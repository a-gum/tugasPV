    private void tabelpasienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpasienMouseClicked
        // TODO add your handling code here:
        int bar = tabelpasien.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();

        tid.setText(a);
        tnama.setText(b);
        System.out.println(c);
        if(c.equals("Laki-laki")){
            rjk1.setSelected(true);
            rjk2.setSelected(false);
        }else {
            rjk1.setSelected(false);
            rjk2.setSelected(true);
        }
        
        talm.setText(d);
        cgd.setSelectedItem(e);
    }//GEN-LAST:event_tabelpasienMouseClicked

    private void tcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tcariActionPerformed
        // TODO add your handling code here:
      Object[] Baris ={"No Identitas", "Nama Pasien", "Jenis Kelamin","Alamat", "Golongan Darah"};
      tabmode = new DefaultTableModel(null, Baris);
      tabelpasien.setModel(tabmode);
      String sql = "SELECT * FROM pasien WHERE nama LIKE '%"+fcari.getText()+"%'";
      try {
        java.sql.Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        while(hasil.next()){
            String a = hasil.getString("id");
            String b = hasil.getString("nama");
            String c = hasil.getString("jk");
            String d = hasil.getString("alamat");
            String e = hasil.getString("goldar");
            String[] data={a,b,c,d,e};
            tabmode.addRow(data);
        }
    } catch (Exception e){
        }
    }//GEN-LAST:event_tcariActionPerformed

    private void butDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butDeleteActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "hapus","Konfirmasi Dialog", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok==0) {
            String sql="DELETE FROM pasien WHERE id='"+tid.getText()+"'";
            try {
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil dihapus");
                kosong();
                tid.requestFocus();
                datatable();
            }catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus" +e);
            }
        datatable();        
        kosong();
        }
        
    }//GEN-LAST:event_butDeleteActionPerformed

    private void butEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butEditActionPerformed
        // TODO add your handling code here:
    try {
            String sql = "update pasien set nama=?, jk=?,alamat=?,goldar=? where id=?";
            PreparedStatement stat = conn.prepareStatement (sql);
            stat.setString(1, tnama.getText());

            String jkel="";
            if(rjk1.isSelected()) jkel="Laki-laki";
            else jkel="Perempuan";
            stat.setString(2, jkel);

            stat.setString(3, talm.getText());
            stat.setString(4, cgd.getSelectedItem().toString());
            stat.setString(5, tid.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
            kosong();
            tid.requestFocus();
            datatable();

        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah"+e);
        }
        datatable();        
        kosong();
    }//GEN-LAST:event_butEditActionPerformed

